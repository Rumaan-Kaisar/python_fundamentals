# running loop over list
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num)


colors = ['red', 'green', 'yelow', 'blue', 5, 'yndoo', True, False]
for clr in colors:
    print(clr)

for i in range(0, len(colors)):
    print(f"{i} : {colors[i]}")

# using while loop
i = 0
while i < len(colors):
    print(f"{i} : {colors[i]}")
    i += 1



