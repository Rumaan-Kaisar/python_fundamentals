# ------------ Dynamically type --------------
""" 
    Reassign variables to different types.
    In Python the data-type of variable is not fixed, we dont need to declare the data type
    The data-type of a variable changes if we give it to different type of values
    Opposite of C++ and java (these are 'statically typed') 
"""

awesomeness = True
print(awesomeness)  # prints True

awesomeness = "La La LA"
print(awesomeness)  # prints La La LA

awesomeness = 3.14159
print(awesomeness)  # prints 3.14159

awesomeness = None
print(awesomeness)  # prints None